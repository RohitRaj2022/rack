        const overlay = document.getElementById('overlay');
        const contactFormContainer = document.getElementById('contactFormContainer');
        const closeBtn = contactFormContainer.querySelector('.close-btn');
        const form = document.getElementById('contactForm');

        // Open the contact form popup
        function openContactForm() {
            // Show overlay and contact form
            overlay.classList.add('active');
            contactFormContainer.setAttribute('aria-hidden', 'false');
            contactFormContainer.classList.remove('closing');
            contactFormContainer.classList.add('opening');
            contactFormContainer.style.display = 'block';

            // Focus the first input for accessibility
            setTimeout(() => {
                contactFormContainer.focus();
                contactFormContainer.querySelector('input, textarea, button').focus();
            }, 300);
        }

        // Close the contact form popup with animation
        function closeContactForm() {
            contactFormContainer.classList.remove('opening');
            contactFormContainer.classList.add('closing');
            contactFormContainer.setAttribute('aria-hidden', 'true');

            overlay.classList.remove('active');

            // Wait for animation to finish then hide
            contactFormContainer.addEventListener('animationend', () => {
                contactFormContainer.style.display = 'none';
                contactFormContainer.classList.remove('closing');
                form.reset();
            }, { once: true });
        }

        // Close when clicking the close button
        closeBtn.addEventListener('click', closeContactForm);

        // Close when clicking outside the form content i.e. on overlay
        overlay.addEventListener('click', closeContactForm);

        // Close on Escape key press
        document.addEventListener('keydown', (e) => {
            if (e.key === 'Escape' && contactFormContainer.style.display === 'block') {
                closeContactForm();
            }
        });

        // Form submission handling
        form.addEventListener('submit', (e) => {
            e.preventDefault();
            if (!form.checkValidity()) {
                form.reportValidity();
                return;
            }
            alert('Thank you for requesting a quote! We will get back to you soon.');
            closeContactForm();
        });

        // Expose functions for external button to trigger
        window.openContactForm = openContactForm;
        window.closeContactForm = closeContactForm;

